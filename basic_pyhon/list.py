# cara membuat list
nama = ["Daffa", "Fawwaz", "Maulana"]
print(nama)

# cara menambahkan data pada list
nama.append("iqbal")
print(nama)

# cara mengakses/mengambil data pada list
print(nama[0])

# cara menghapus data pada list
erase = nama[0]
nama.remove(erase)
print(nama)

# cara mengedit data pada list
nama[0] = "Fawwaz_new"
print(nama)

# cara melihat berapa banyak data dalam list
print(len(nama))